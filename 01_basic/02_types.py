###
# 02 - types()
# Pyton tiene varios tipos de datos, entre ellos:
# int, float, str, bool, list, tuple, dict, set, dict, range, complex...
###

print("int:")
print(type(10))
print(type(0))
print(type(-1))
print(type(123412434123413241))

print("float:")
print(type(3.4))
print(type(1.0))
print(type(1e3))

print("complex:")
print(type(1 + 2j))

print("string:")
print(type("Hola"))

print("bool:")
print(type(True))
print(type(False))
print(type(1 > 2))

print("NoneType:")
print(type(None))
